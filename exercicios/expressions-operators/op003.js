//Operadores lógicos (logical operators)

// 2 valores booleanos, quando verificados, resultara em verdadeiro ou falso.

let pao = true
let queijo = false 

// AND(E) > &&
console.log(pao && queijo)


// OR(OU) > ||
console.log(pao || queijo)

// NOT(NÃO) > !   (Operador de negação)
console.log(!queijo)
